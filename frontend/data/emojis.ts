export function searchUnicodeEmojis (query: string, limit = 8): UnicodeEmoji[] {
  if (!query) return []
  const q = query.toLowerCase()
  return unicodeEmojis
    .filter(e =>
      e.shortcode.includes(q) ||
      e.keywords.some(k => k.includes(q))
    )
    .slice(0, limit)
}
